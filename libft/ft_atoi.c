int	ft_atoi(const char *str)
{
	int	i;
	int sign;

	sign = 1;
	i = 0;
	while (*str == '\t' || *str == '\n' || *str == ' ' || *str == '\r' ||
			*str == '\f' || *str == '\0' || *str == '\v')
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str != 0 && *str >= '0' && *str <= '9')
	{
		i *= 10;
		i += (*str - '0');
		str++;
	}
	return (i * sign);
}
