#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	char *occ;

	occ = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			occ = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (occ);
}
