#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	count;

	i = 0;
	count = 0;
	while (s1[i] != '\0')
		i++;
	while (*s2 != '\0' && count < n)
	{
		s1[i] = *s2;
		s2++;
		i++;
		count++;
	}
	s1[i] = '\0';
	return (s1);
}
