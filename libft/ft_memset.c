#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*str1;

	str1 = b;
	i = 0;
	while (i < len)
	{
		str1[i] = (unsigned)c;
		i++;
	}
	return (b);
}
